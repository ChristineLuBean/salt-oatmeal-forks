const instructions = [
  'Click get prompt, then take 3 minutes to write a list inspired by it.',
  'Click get prompt, then take 5 minutes to write a story about it using 3-5 words from your list.',
  'Click get prompt, then take 10 minutes to write another story inspired by your first story.',
];
const instructionsPara = document.querySelector('.instructions');
const promptText = document.querySelector('.prompt-text');
const startButton = document.querySelector('.start');
const stopButton = document.querySelector('.stop');
const timerSpan = document.querySelector('.timer');

let round = 0;
let timerId = null;
let timeLeft = 5;
// let timeLeft = 180; // 3 minutes in seconds

startButton.addEventListener('click', startTimer);
stopButton.addEventListener('click', newRound);
// Hide the stop button initially
stopButton.style.display = 'none';

function displayInstructions() {
  if (round === 0) {
    instructionsPara.textContent = instructions[0];
  }
  if (round === 1) {
    instructionsPara.textContent = instructions[1];
  }
  if (round === 2) {
    instructionsPara.textContent = instructions[2];
  }
}
function displayPrompt() {
  if (round === 0) {
    promptText.textContent =
      roundOnePrompts[Math.floor(Math.random() * roundOnePrompts.length)];
  } else if (round === 1) {
    promptText.textContent =
      roundTwoPrompts[Math.floor(Math.random() * roundTwoPrompts.length)];
  } else {
    promptText.textContent =
      roundThreePrompts[Math.floor(Math.random() * roundThreePrompts.length)];
  }
}
function stopTimer() {
  clearInterval(timerId);
  timerId = null;
  stopButton.style.display = 'inline-block';
  startButton.style.display = 'none';
}
function startTimer() {
  if (round === 0) {
    timeLeft = 180; // Set timer to 5 seconds for round 0
  } else if (round === 1) {
    timeLeft = 300; // Set timer to 10 seconds for round 1
  } else {
    timeLeft = 600; // Set timer to 15 seconds for round 2
  }
  timerId = setInterval(countDown, 1000);
  startButton.style.display = 'none';
  displayPrompt();
  updateTimerDisplay();
}

function countDown() {
  timeLeft--;
  updateTimerDisplay();
  if (timeLeft <= 0) {
    stopTimer();
    playAlarm();
  }
}

function updateTimerDisplay() {
  const minutes = Math.floor(timeLeft / 60);
  const seconds = timeLeft % 60;
  timerSpan.textContent = `${minutes}:${seconds.toString().padStart(2, '0')}`;
}

function playAlarm() {
  const audio = new Audio('./notification.mp3');
  audio.play();
}

function newRound() {
  stopButton.style.display = 'none';
  startButton.style.display = 'inline-block';
  if (round === 2) {
    round = 0;
  } else {
    round++;
  }
  promptText.textContent = '';
  displayInstructions();
}

displayInstructions();
